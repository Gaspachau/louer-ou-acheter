export function findVille(query) {
  if (!query || query.length < 2) return [];

  const normalize = (str) =>
    str
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '')
      .toLowerCase();

  const normalizedQuery = normalize(query);

  return VILLES.filter((ville) =>
    normalize(ville.nom).includes(normalizedQuery)
  ).slice(0, 8);
}
